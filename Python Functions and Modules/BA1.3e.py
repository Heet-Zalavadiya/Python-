import geometry

radius = 5
circle_area = geometry.area_of_circle(radius)
print(f"Area of the circle with radius {radius}: {circle_area:.2f}")

length = 8
width = 4
rectangle_area = geometry.area_of_rectangle(length,width)
print(f"Area of the rectangle with length  {length} and width {width}: {rectangle_area:.2f}")

base = 6
height = 3
triangle_area = geometry.area_of_triangle(base,height)
print(f"Area of the triangle with base {base} and height {height}: {triangle_area:.2f}")


